skills = (
    'Стремительный прыжок',
    'Электрический выстрел',
    'Ледяной удар',
    'Стремительный удар',
    'Кислотный взгляд',
    'Тайный побег',
    'Ледяной выстрел',
    'Огненный заряд'
    )


def main():
    file_path = "персонажи"
    os.makedirs(file_path, exist_ok=True)
    
    for number in range(10):
        random_name = Faker("ru_RU").first_name_male()
        random_surname = Faker("ru_RU").last_name_male()
        random_job = Faker("ru_RU").job()
        random_city = Faker("ru_RU").city()
        
        current_skills = sample(skills, 3)
        runic_skills = []
        for skill in current_skills:
            for letter in skill:
                skill = skill.replace(letter, runic_alphabet[letter])
            runic_skills.append(skill)
        context = {
            "first_name": random_name,
            "last_name": random_surname,
            "job": random_job,
            "town": random_city,
            "strength": randint(3,18),
            "agility": randint(3,18),
            "endurance": randint(3,18),
            "intelligence": randint(3,18),
            "luck": randint(3,18),
            "skill_1": runic_skills[0],
            "skill_2": runic_skills[1],
            "skill_3": runic_skills[2]
        }
        file_operations.render_template("charsheet.svg", f"{file_path}/character{number+1}.svg", context)
        
        
if __name__ == '__main__':
    main()
